package net.beifeng.mobile_scm.basic.entity;

public class AccessoryType {

    private String accessorytypeid;

    private String typename;

    private String unit;

    private String spec;

    private String manufacturer;

    private String remark;

    private String brand;

    public String getAccessorytypeid() {
        return accessorytypeid;
    }

    public void setAccessorytypeid(String accessorytypeid) {
        this.accessorytypeid = accessorytypeid;
    }

    public String getTypename() {
        return typename;
    }

    public void setTypename(String typename) {
        this.typename = typename;
    }

    public String getUnit() {
        return unit;
    }

    public void setUnit(String unit) {
        this.unit = unit;
    }

    public String getSpec() {
        return spec;
    }

    public void setSpec(String spec) {
        this.spec = spec;
    }

    public String getManufacturer() {
        return manufacturer;
    }

    public void setManufacturer(String manufacturer) {
        this.manufacturer = manufacturer;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }
}